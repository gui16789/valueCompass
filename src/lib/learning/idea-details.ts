export function getIdeaDetail(idea: string, node: KnowledgeNode): IdeaDetail {
  const known = ideaDetails[idea];

  if (known) {
    return known;
  }

  return {
    explanation: `${idea} 是理解「${node.title}」的一个关键抓手。你可以先把它翻译成一个可验证的问题：它如何影响企业赚钱、现金流、风险或估值？然后回到财报、行业数据和竞争格局里找证据。`,
    example: `以 A 股公司研究为例，不要只记住「${idea}」这个词，而要写出它在具体公司上的表现：对应哪些财务指标、哪些经营现象、哪些反面证据，以及哪些变化会推翻你的判断。`
  };
}
